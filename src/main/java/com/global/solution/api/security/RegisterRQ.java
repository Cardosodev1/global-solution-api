package com.global.solution.api.security;

import jakarta.validation.constraints.Email;
import jakarta.validation.constraints.NotBlank;
import jakarta.validation.constraints.NotNull;

public record RegisterRQ(

        @NotNull
        @NotBlank
        String name,

        @NotBlank
        @Email
        String email,

        @NotNull
        @NotBlank
        String password

) {
}
